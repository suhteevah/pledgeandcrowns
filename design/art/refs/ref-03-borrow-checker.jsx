// Replace any stray '&' or 'm' (rune hints) with main teal — those are
// just placeholder runes; we want them to render as the embroidery color.
const REF03_NORMALIZED = REF03_GRID.map((row) =>
  row.replace(/&/g, 'T').replace(/m/g, 'T')
).map((row) => {
  // pad to 64 chars in case any row drifted
  if (row.length < 64) return row + '.'.repeat(64 - row.length);
  if (row.length > 64) return row.slice(0, 64);
  return row;
});

window.REF03 = function REF03({ scale = 4 }) {
  return <PixelArt grid={REF03_NORMALIZED} scale={scale} title="REF-03 Borrow Checker" />;
};
